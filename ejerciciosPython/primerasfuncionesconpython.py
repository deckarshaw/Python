# MULTIPLICACION

def multiplicar(numeroUno,numeroDos):

	return numeroUno * numeroDos


print(multiplicar(20,5))

# SUMAR

def sumar(numero1,numero2):

	return numero1 + numero2

print(sumar(1050,50))



# RESTAR

def restar(numero1,numero2):

	return numero1 - numero2

print(restar(100,5))


#DIVISION

def dividir(numeroUno,numeroDos):

	return numeroUno - numeroDos

print(dividir(25,5))

